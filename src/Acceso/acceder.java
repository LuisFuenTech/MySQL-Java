    //Este método nos retorna la conexión a la base de datos
    public static Connection getConnection(){
        con = null; //con asignarle null
        
        try{
            
            Class.forName("java.sql.Driver"); //Cargar el driver
            //Mandarle la url, el usuario y la contraseña a con por el driverManager 
            con = DriverManager.getConnection(URL, USERNAME, PASSWORD);
            //Mensaje para notificar la conexión exitosa
            JOptionPane.showMessageDialog(null, "Conexión establecida");
            
        }catch(ClassNotFoundException | SQLException e){
            System.out.println(e); //Mensaje para imprimir algún error
        }
        
        return con; //Retornar con y toda la información de la dirección.
    }
    
