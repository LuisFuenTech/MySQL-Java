    private void conectar_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conectar_buttonActionPerformed
        try{
            
            con = null; //Definir nulo la variable
            con = getConnection(); //Obtener lo que retorna getConnection();
            
            PreparedStatement ps; /*Variable PreparedStatement para poder enviar
            la instrucción al servidor por sql, para eliminar, crear, modificar 
            la base de datos*/
            ResultSet res;//Variable ResultSet trae los resultados de la consulta
            
            /*A ps se le asigna la conexión con la instrucción sql. La table de
            la base de datos se llama "persona", seleccionare de esa tabla*/
            ps = con.prepareStatement("SELECT * FROM persona");
            res = ps.executeQuery(); //ps se ejecuta con query.
            
            if(res.next()){//Si existen datos
                //Extraer nombre, apellido y teléfono de "persona".
                JOptionPane.showMessageDialog(null, res.getString("nombre") + " " +  
                                                    res.getString("apellido") + " " +
                                                    res.getString("telefono"));
            }else{
                JOptionPane.showMessageDialog(null, "No existen datos");
            }
            
            con.close();//Cerrar conexión
            res.close();//Cerrar el query.
            
        }catch(SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_conectar_buttonActionPerformed

